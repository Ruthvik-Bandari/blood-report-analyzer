// ─── Helper: Find reference range by name ──────────────────

export function findReferenceRange(name: string): ReferenceRange | null {
  const normalized = name.toLowerCase().trim();
  return (
    REFERENCE_RANGES.find(
      (r) =>
        r.standardName.toLowerCase() === normalized ||
        r.aliases.some((a) => a === normalized)
    ) ?? null
  );
}

// ─── Helper: Normalize biomarker name to standard ──────────

export function normalizeToStandardName(name: string): string {
  const range = findReferenceRange(name);
  return range?.standardName ?? name;
}
